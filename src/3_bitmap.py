def draw_map(bitmap, pattern):
    lines = bitmap.splitlines()
    pat_len = len(pattern)
    for line in lines:
        for i, bit in enumerate(line):
            if bit == ' ':
                print(' ', end='')
            else:
                print(pattern[i%pat_len], end='')
        print("")


